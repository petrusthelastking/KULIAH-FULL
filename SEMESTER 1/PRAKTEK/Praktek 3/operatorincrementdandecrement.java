public class operatorincrementdandecrement {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("1 : " + i);
        System.out.println("++1 : " + ++i);
        System.out.println("1++ : " + i++);
        
        System.out.println("--1 : " + --i);
        System.out.println("1-- : " + i--);
        System.out.println("1 : " + i);
    }
}